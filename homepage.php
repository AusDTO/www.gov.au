    <div class="page-header">
      <h2>Local Government entry points</h2>
      <p class="lead">
        The six states and the Northern Territory have established one further level of government.
        Local governments (also known as city, shire, town or municipal councils) handle community needs like waste collection,
        public recreation facilities and town planning.
      </p>
      <p class="lead">
        The <a href="http://www.alga.asn.au/">Australian Local Government Association</a> is the national voice of local government,
        representing 560 councils across the country. State local government directories provide links to local council sites:
      </p>
      <ul class="lead">
        <li><a href="http://www.olg.nsw.gov.au/">New South Wales local government directory</a></li>
        <li><a href="http://www.localgovernment.nt.gov.au/home/council_information">Northern Territory local council information</a></li>
        <li><a href="http://www.dlg.qld.gov.au/local-government-directory.html">Queensland local government directory</a></li>
        <li><a href="http://www.lga.sa.gov.au/page.aspx?u=210">South Australia local council information</a></li>
        <li><a href="http://www.dpac.tas.gov.au/divisions/local_government/local_government_directory">Tasmania local government directory</a></li>
        <li><a href="http://www.dtpli.vic.gov.au/local-government">Victoria local council information</a></li>
        <li><a href="http://www.dlg.wa.gov.au/Content/Directory/">Western Australia local government directory</a></li>
      </ul>
