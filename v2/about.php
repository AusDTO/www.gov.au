<h2>Choosing which jurisdiction</h2>
<p>If you are unfamiliar with the Australian Government, the national government is known as the Australian Government. The responsibilities of the Australian Government include national matters such as external trade and commerce, quarantine, currency, patents, immigration, defence, telecommunications, and the provision of welfare and other assistance payments.</p>

<p>Other matters not provided for under the <a href="http://www.aph.gov.au/senate/general/constitution/index.htm">Australian Constitution</a> as a Commonwealth responsibility are the responsibility of the states - New South Wales, Victoria, Queensland, South Australia, Western Australia and Tasmania. Two mainland territories - the Northern Territory and the Australian Capital Territory - are self-governing and have similar responsibilities to the states.</p>

<p>The states and territories are responsible for matters such as policing, public schools, roads and traffic including driver's licences and vehicle registrations, public hospitals, public housing, and business regulation.</p>

<p>Local governments (also known as local councils) handle community needs like waste collection, public recreation facilities and town planning. The states and the Northern Territory each have many local governments within their borders. The state or territory government defines the powers of the local governments, and decides what geographical areas those governments are responsible for. Uniquely in Australia, the Australian Capital Territory combines both state and local government functions.</p>

